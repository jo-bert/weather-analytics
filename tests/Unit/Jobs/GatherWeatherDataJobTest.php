  /** @test */
  public function handle_method_executes_successfully()
  {
    for ($j = -7; $j <= 2; $j++) {
      $date = now()->addDays($j)->format('Y-m-d');
      Http::fake(
        ["http://api.weatherapi.com/v1/history.json?key={$this->weatherApiKey}&q={$this->city}&dt={$date}" => Http::response($this->historyResponse, 200)]
      );
    }
    Http::fake([
      "http://api.weatherapi.com/v1/forecast.json?key={$this->weatherApiKey}&q={$this->city}&days=1&aqi=no&alerts=no" => Http::response($this->forecastResponse, 200),
    ]);
    Log::shouldReceive('channel')->with('job')->andReturnSelf();
    Log::shouldReceive('debug')->with("Gather Data for city {$this->city}");
    Log::shouldReceive('info')->with("Location created: {$this->city}")->atMost(2);
    for ($d = 7; $d <= 16; $d++) {
      for ($i = 0; $i <= 23; $i++) {
        $hour = sprintf("%02d", $i);
        $dString = $d < 10 ? "0" . $d : $d;
        Log::shouldReceive('info')->with("Hourly forecast created/updated for location: {$this->city} in 2025-02-{$dString} {$hour}:00:00");
      }
    }
    Log::shouldReceive('info')->with("Error code found when trying to query 2025-02-16 for history {$this->city}");

    // Create a job instance
    $job = new GatherWeatherData($this->city);

    // Act
    $job->handle();

    // Assert
    $this->assertDatabaseHas('locations', ['name' => $this->city]);
    $forecastDays = $this->forecastResponse['forecast']['forecastday'];
    foreach ($forecastDays as $forecastDay) {
      $this->assertDatabaseHas('hourly_forecasts', ['date_epoch' => $forecastDay['date_epoch']]);
    }
  }

